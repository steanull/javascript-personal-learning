"use strict"
/* Числа могут быть отрицательными.
Для этого перед числом необходимо поставить знак минус:
 */

let a = -1;
alert (a); // Выведет -1

/*
    Знак минус можно писать как к числам, так и к переменным:
 */

let a2 = 1;
let b2 = -a2; // записали в b содержимое a с обратным знаком
alert (b2); // Выведет -1

let a3 = 1;
alert (-a3); // выведет -1

let a4 = -100;
alert (a4);

let a5 = -20;
alert (-a5);