"use strict"
/*
    Математические операции можно производить не только над числами, но и над переменными.
    Сложим, к примеру, значения двух переменных
 */

let a = 1;
let b = 2;
alert (a + b);

/*
    Не обязательно сразу выводить результат операции, можно вначале записать его в какую нибудь
    переменную, а уже потом вывести значение этой переменной
 */

let c = 1;
let d = 2;
let e = c + d;
alert (e);

let num1 = 10;
let num2 = 2;
alert (num1 + num2);
alert (num1 - num2);
alert (num1 * num2);
alert (num1 / num2);

let num3 = 10;
let num4 = 2;
let result = num4 + num3;
alert (result);

let num5 = 1;
let num6 = 2;
let num7 = 3;
alert (num5 + num6 + num7);

let num8 = 10;
let num9 = 5;

let result2 = num8 - num9;

let num10 = 7;
let sum = num10 + result2;
alert (sum);