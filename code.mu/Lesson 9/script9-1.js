"use strict"

/*
    Существует специальный оператор %, с помощью которого можно найти остаток от деления одного чилсла на другое:
 */

alert (10 % 3); // Выведет 1

/*
    Если одно число делится нацело на другое - остаток будет равен 0:
 */

alert (10 % 2); // Выведет 0

/*
    Оператор %, конечно же, можно применять не только к числам, но и к переменным:
 */

let a = 10;
let b = 3;
alert (a % b); // Выведет 1

let a2 = 13;
let b2 = 5;
alert (a2 % b2); // Выведет 3