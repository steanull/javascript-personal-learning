"use strict"
/*
    Для сложения строк, так же как и для сложения чисел, используется оператор +:
 */

let str = 'abc' + 'def'; // Складываем две строки
alert(str);  // Выведет 'abcdef'

let str2 = 'abc';
let str3 = 'def';
alert(str2 + str3);  // Выведет abcdef

let str4 = 'abc';
let str5 = 'def';
alert(str4 + "!!!" + str5); // выведет abc!!!def

let str6 = 'abc';
let str7 = 'def';
alert(str6 + ' ' + str7); // Выведет abc def

let str8 = 'abc';
alert(str8 + ' def'); // Выведет abc def

let str9 = '!!!'
alert(str9);

let str10 = 'java';
let str11 = 'script'
alert (str10 + str11);

let str12 = 'hello';
let str13 = 'world'
alert (str12 + ' ' + str13);


